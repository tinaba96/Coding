N = int(input())
d = [1] * 9
r = 998244353

for i in range(N - 1):
  p    =   [0] * 9
  p[0] = (d[0] + d[1]       ) % r
  p[1] = (d[0] + d[1] + d[2]) % r
  p[2] = (d[1] + d[2] + d[3]) % r
  p[3] = (d[2] + d[3] + d[4]) % r
  p[4] = (d[3] + d[4] + d[5]) % r
  p[5] = (d[4] + d[5] + d[6]) % r
  p[6] = (d[5] + d[6] + d[7]) % r
  p[7] = (d[6] + d[7] + d[8]) % r
  p[8] = (d[7] + d[8]       ) % r
  d    =  p
  
print(sum(d) % r)
